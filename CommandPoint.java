    public @DataReflector Set<Modifier> modifierSet();
      // Cf. `ResourceIndicant.qualifiers`, whose form enables easier extension.



   // ━━━  P o i n t  ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━


    /** The point descriptor.
      */
